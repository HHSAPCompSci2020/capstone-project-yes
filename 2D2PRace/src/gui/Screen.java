package gui;
/**
 * Abstract class used as preset for the other screens
 * 
 * @author Connor Chow
 * @version 5/7/21
 */
public abstract class Screen {
	
	/**
	 * Constructs a default screen
	 */
	public Screen() {
		
	}
	
	/**
	 * setups the screen
	 */
	public void setup() {
		
	}
	
	/**
	 * draws the screen
	 */
	public void draw() {
		
	}
	
	/**
	 * Method to detect when the user presses the mouse and actions the screen takes
	 */
	public void mousePressed() {
		
	}
	
	/**
	 * Method to detect when the user moves the mouse and actions the screen takes
	 */
	public void mouseMoved() {
		
	}
	
	/**
	 * Method to detect when the user drags the mouse and actions the screen takes
	 */
	public void mouseDragged() {
		
	}
	
	/**
	 * Method to detect when the user releases the mouse and actions the screen takes
	 */
	public void mouseReleased() {
		
	}
}
